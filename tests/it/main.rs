//! Single integration-test binary.
//!
//! All integration tests are compiled and linked into this one crate
//! (`it`) instead of one binary per `tests/*.rs` file. Cargo links a
//! separate executable for every top-level file in `tests/`, so ~230
//! files meant ~230 link steps and ~230 copies of the shared `common`
//! tree; collapsing them into this module tree cuts compile time, link
//! time, and `target/` size. Each former `tests/<name>.rs` is now
//! `tests/it/<name>.rs`, declared below as a module.
//!
//! nextest still addresses individual tests by name
//! (`it::<module>::<test>`); filters by test name are unaffected.

